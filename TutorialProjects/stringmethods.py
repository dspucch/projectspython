course = 'Python for beginners'
print(len(course))
#len function prints length of the variable
print(course.upper())#converts string to uppercase
print(course.lower())#converts string to lowercase
print(course.find('o'))#prints index of character
print(course.find('beginners'))
print(course.replace('beginners', 'Absolute Beginners')) #replaces first string with second string
#^^ can also replace single characters
print('Python' in course) #validating if "python" is in the variable
#in operator produces a boolean value

